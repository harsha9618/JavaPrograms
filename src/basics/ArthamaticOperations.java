package basics;

public class ArthamaticOperations {
	public static void main(String args[]) {
		
		int a=18;
		int b=20;
		int c=a+b;
		int d=a-b;
		int e=a*b;
		int f=a%b;
		int g=a/b;
		
		System.out.println("C is " +c);
		System.out.println("D is " +d);
		System.out.println("E is " +e);
		System.out.println("F is " +f);
		System.out.println("G is " +g);
		
	}

}
